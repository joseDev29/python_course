# min(number, number, ...numbers)
# min(iterable)
my_min = min(1, 2, 3, 4)  # out: 1
my_min2 = min([1, 2, 3, 4])  # out: 1
# first capital letters
my_min3 = min("carlos")  # out: 'a'
my_min4 = min("Carlos")  # out: 'C'

# max(number, number, ...numbers)
# max(iterable)
my_max = max(1, 2, 3, 4)  # out: 4
my_max2 = max([1, 2, 3, 4])  # out: 4
# first capital letters
my_max3 = max("carlos")  # out: 's'
my_max4 = max("Carlos")  # out: 's'
